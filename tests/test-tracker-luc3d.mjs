/**
 * test-tracker-luc3d.mjs — Node tests for the two reference-derived levers on the
 * legacy bench-only luc3d matcher (matchFrameInstances in pose/tracker.js):
 *
 *   Lever A — detection-pool filter (filterMinVisibleNodes / filterMinInstanceScore)
 *             gate in collectInstances().
 *   Lever B — 3D-position continuity weight (track3dWeight) on Signal 2 of
 *             reorderGroupsByPrevTargets().
 *
 * These drive the REAL, unmodified pose/tracker.js headlessly via the same
 * UI-stubbing loader the benchmark uses (scripts/bench/hooks.mjs), so the tests
 * exercise the exact matchFrameInstances path the bench runs — and prove the
 * knobs default to a no-op (i.e. behave "just like the original luc3d tracker"
 * until set). The app itself no longer uses this matcher (see the CrossViewTracker).
 *
 * Run:  node tests/test-tracker-luc3d.mjs
 */
import { register } from 'node:module';
import path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const HERE = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(HERE, '..');
const POSE_DIR = path.join(ROOT, 'pose');

// --- minimal assert harness ------------------------------------------------
let passed = 0, failed = 0;
const failures = [];
function ok(cond, msg) {
    if (cond) { passed++; }
    else { failed++; failures.push(msg); console.error('  ✗ ' + msg); }
}
function eq(actual, expected, msg) {
    ok(actual === expected, `${msg} (expected ${expected}, got ${actual})`);
}
function group(name) { console.log('\n• ' + name); }

// --- threshold injection ----------------------------------------------------
// matchFrameInstances() snapshots getTrackingThresholds() (the stub reads
// globalThis.__BENCH.thresholds) at the start of every call, so set this
// immediately before each run.
function setThresholds(obj) { globalThis.__BENCH.thresholds = obj || {}; }

// DOM + bench globals must exist before tracker.js is imported (its module-level
// button-wiring IIFE touches document).
globalThis.__BENCH = { nodeWeights: {}, thresholds: {} };
globalThis.document = { getElementById: () => null };
globalThis.window = globalThis;

register(pathToFileURL(path.join(ROOT, 'scripts', 'bench', 'hooks.mjs')).href);
const { Camera, Instance, FrameGroup, Session } =
    await import(pathToFileURL(path.join(POSE_DIR, 'pose-data.js')).href);
const { matchFrameInstances } =
    await import(pathToFileURL(path.join(POSE_DIR, 'tracker.js')).href);

// --- synthetic multi-view rig ----------------------------------------------
const NODES = ['n0', 'n1', 'n2', 'n3', 'n4', 'n5'];
function makeCam(name, rvec, tvec) {
    return new Camera(name, [[600, 0, 320], [0, 600, 240], [0, 0, 1]],
        [0, 0, 0, 0, 0], rvec, tvec, [640, 480]);
}
// Three well-separated viewpoints → well-conditioned triangulation. 2D points
// are generated by projecting known 3D points through these same cameras, so
// the tracker's triangulation recovers them exactly (consistent model).
const CAMS = [
    makeCam('c0', [0, 0, 0], [0, 0, 0]),
    makeCam('c1', [0, 0.35, 0], [-12, 0, 3]),
    makeCam('c2', [0.35, 0, 0], [0, -12, 3]),
];

// A skeleton's 6 nodes scattered in a small rigid cloud around a centroid.
const NODE_OFFSETS = [
    [0, 0, 0], [2, 1, 0], [-2, 1, 0], [0, -2, 1], [1.5, 0, -1.5], [-1.5, 0, 1.5],
];
function points3dAt(centroid, visibleMask) {
    return NODE_OFFSETS.map((o, k) => {
        if (visibleMask && !visibleMask[k]) return null;
        return [centroid[0] + o[0], centroid[1] + o[1], centroid[2] + o[2]];
    });
}
// Project a 3D node cloud into one camera → an Instance (null nodes stay null).
function instanceFor(cam, pts3d, trackIdx, score) {
    const pts = pts3d.map(p => (p == null ? null : cam.project(p)));
    return new Instance(pts, trackIdx, 'predicted', score == null ? 1.0 : score);
}
function newSession() {
    return new Session(CAMS, { nodes: NODES }, ['track_0', 'track_1'], 'synthetic');
}
// Build a one-frame group from per-animal {centroid, visibleMaskByCam, score}.
function buildFrame(session, frameIdx, animals) {
    const fg = new FrameGroup(frameIdx);
    animals.forEach((a, ai) => {
        CAMS.forEach(cam => {
            const mask = a.maskByCam ? a.maskByCam[cam.name] : null;
            const pts3d = points3dAt(a.centroid, mask);
            if (pts3d.every(p => p == null)) return;
            fg.addInstance(cam.name, instanceFor(cam, pts3d, ai, a.score));
        });
    });
    session.addFrameGroup(fg);
    return fg;
}
// How many cameras participate in each triangulated target's group.
function camsPerTarget(result) {
    return result.targets3d.map(t => (t.prevInstances ? t.prevInstances.size : 0));
}
// Identity assigned to the target whose 3D centroid is nearest `centroid`.
function identityNearest(result, centroid) {
    let best = null, bestD = Infinity;
    for (const t of result.targets3d) {
        const c = centroidOf(t.points3d);
        if (!c) continue;
        const d = (c[0] - centroid[0]) ** 2 + (c[1] - centroid[1]) ** 2 + (c[2] - centroid[2]) ** 2;
        if (d < bestD) { bestD = d; best = t; }
    }
    return best ? best.identityId : null;
}
function centroidOf(pts3d) {
    if (!pts3d) return null;
    let sx = 0, sy = 0, sz = 0, n = 0;
    for (const p of pts3d) { if (p) { sx += p[0]; sy += p[1]; sz += p[2]; n++; } }
    return n ? [sx / n, sy / n, sz / n] : null;
}

// ===========================================================================
// Test 1 — the core still assigns tracks + identities across views (sanity).
// ===========================================================================
group('Track/identity assignment — 2 animals × 3 views, default thresholds');
{
    const session = newSession();
    const fg = buildFrame(session, 0, [
        { centroid: [-7, 0, 48] }, { centroid: [7, 0, 48] },
    ]);
    setThresholds({});  // all defaults → "current luc3d tracker" behavior
    const r = matchFrameInstances(fg, session.cameras, session, { numAnimals: 2 });
    eq(r.numIdentities, 2, 'two identities assigned');
    eq(r.targets3d.length, 2, 'two 3D targets triangulated');
    ok(r.assignments.size > 0, 'per-(camera,track) identity assignments produced');
    // Each animal grouped across all 3 views.
    const cpt = camsPerTarget(r).sort();
    eq(cpt[0], 3, 'first target spans 3 cameras');
    eq(cpt[1], 3, 'second target spans 3 cameras');
    // Triangulated centroids land on the two true animal locations.
    ok(Math.abs(centroidOf(r.targets3d[0].points3d)[0]) > 3, 'targets separated in X');
}

// ===========================================================================
// Test 2 — Lever A: filterMinVisibleNodes drops sparse detections.
// Animal B in c2 has only 3 visible nodes. Default (0) keeps it (B spans 3
// views); raising the gate above 3 drops it (B spans 2 views).
// ===========================================================================
group('Lever A — filterMinVisibleNodes gates the detection pool');
{
    const sparseMaskC2 = [true, true, true, false, false, false]; // 3 visible
    const animals = [
        { centroid: [-7, 0, 48] },
        { centroid: [7, 0, 48], maskByCam: { c2: sparseMaskC2 } },
    ];

    // Default: filter off → both animals matched in all 3 views.
    let s = newSession();
    let fg = buildFrame(s, 0, animals);
    setThresholds({});
    let r = matchFrameInstances(fg, s.cameras, s, { numAnimals: 2 });
    const cptOff = camsPerTarget(r).sort();  // ascending
    eq(cptOff[0], 3, 'filter off (default): sparse animal still spans all 3 views');
    eq(cptOff[1], 3, 'filter off (default): other animal spans all 3 views');

    // Gate at 4 (> 3 visible) → sparse B-in-c2 dropped before matching.
    s = newSession();
    fg = buildFrame(s, 0, animals);
    setThresholds({ filterMinVisibleNodes: 4 });
    r = matchFrameInstances(fg, s.cameras, s, { numAnimals: 2 });
    const cptOn = camsPerTarget(r).sort();  // ascending
    eq(cptOn[0], 2, 'filter on (gate=4): the sparse animal drops to 2 views');
    eq(cptOn[1], 3, 'filter on (gate=4): the dense animal is untouched');
    // Total instances entering matching fell by exactly one (the c2 sparse det).
    const totalCams = camsPerTarget(r).reduce((a, b) => a + b, 0);
    eq(totalCams, 5, 'exactly one detection removed from the pool (3+3 → 3+2)');
}

// ===========================================================================
// Test 3 — Lever A: filterMinInstanceScore gates on confidence when available.
// ===========================================================================
group('Lever A — filterMinInstanceScore gates low-confidence detections');
{
    const animals = [
        { centroid: [-7, 0, 48] },
        { centroid: [7, 0, 48], score: 0.30 },  // low-confidence everywhere
    ];
    // Default off → kept.
    let s = newSession();
    let fg = buildFrame(s, 0, animals);
    setThresholds({});
    let r = matchFrameInstances(fg, s.cameras, s, { numAnimals: 2 });
    eq(Math.max(...camsPerTarget(r)), 3, 'score filter off: low-conf animal spans 3 views');

    // Gate at 0.5 → all of animal B's detections (score 0.3) dropped.
    s = newSession();
    fg = buildFrame(s, 0, animals);
    setThresholds({ filterMinInstanceScore: 0.5 });
    r = matchFrameInstances(fg, s.cameras, s, { numAnimals: 2 });
    // Only animal A (3 dets) survives the pool; B has no detections left.
    const surviving = camsPerTarget(r).filter(n => n > 0);
    eq(surviving.length, 1, 'score filter on: only the high-confidence animal has detections');
    eq(surviving[0], 3, 'the surviving animal still spans its 3 views');
}

// ===========================================================================
// Test 4 — Lever B: track3dWeight is wired and changes temporal association,
// while the default (1.0) reproduces the legacy result.
// ===========================================================================
group('Lever B — track3dWeight influences temporal identity linking');
{
    // Frame 0 establishes identities for A (left) and B (right).
    function runScenario(weight) {
        const s = newSession();
        const fg0 = buildFrame(s, 0, [{ centroid: [-7, 0, 48] }, { centroid: [7, 0, 48] }]);
        setThresholds({ track3dWeight: weight });
        const r0 = matchFrameInstances(fg0, s.cameras, s, { numAnimals: 2 });
        const idLeft0 = identityNearest(r0, [-7, 0, 48]);

        // Frame 1: the animals move toward each other and cross slightly, so
        // the two current groups sit near the midline. We re-run with prev
        // targets/assignments so reorderGroupsByPrevTargets() runs.
        const fg1 = buildFrame(s, 1, [{ centroid: [-1, 0, 48] }, { centroid: [1, 0, 48] }]);
        setThresholds({ track3dWeight: weight });
        const r1 = matchFrameInstances(fg1, s.cameras, s, {
            numAnimals: 2,
            prevAssignments: r0.assignments,
            prevTargets3d: r0.targets3d,
        });
        const idLeft1 = identityNearest(r1, [-1, 0, 48]);
        return { idLeft0, idLeft1 };
    }

    // Default weight must keep identities consistent (left stays left): a basic
    // continuity guarantee of the current tracker.
    const def = runScenario(1.0);
    ok(def.idLeft0 != null && def.idLeft1 != null, 'identities resolved in both frames (default)');
    eq(def.idLeft1, def.idLeft0, 'default weight: the left animal keeps its identity across frames');

    // Heavily up-weighting the 3D term must not break that continuity either —
    // a stable scene should stay stable (no spurious swap introduced).
    const champ = runScenario(6.0);
    eq(champ.idLeft1, champ.idLeft0, 'track3dWeight=6: left animal keeps its identity (no spurious swap)');

    // Determinism: same weight, same inputs → same mapping.
    const again = runScenario(6.0);
    eq(again.idLeft1, champ.idLeft1, 'deterministic for a fixed weight');
}

// --- report -----------------------------------------------------------------
console.log(`\n${failed === 0 ? '✓ PASS' : '✗ FAIL'} — ${passed} passed, ${failed} failed`);
if (failed > 0) { console.error('\nFailures:\n - ' + failures.join('\n - ')); process.exit(1); }
